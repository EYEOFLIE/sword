function GetItemMult(item,equipKind)

	local itemMult = 0
	if equipKind == ITEM_KIND_SWORD then
		if item:GetSpecialValueFor("weapon_mult_sword") ~= nil then
			itemMult = item:GetSpecialValueFor("weapon_mult_sword")
		end
	end
	if equipKind == ITEM_KIND_KNIFE then
		if item:GetSpecialValueFor("weapon_mult_blade") ~= nil then
			itemMult = item:GetSpecialValueFor("weapon_mult_blade")
		end
	end
	if equipKind == ITEM_KIND_STICK then
		if item:GetSpecialValueFor("weapon_mult_stick") ~= nil then
			itemMult = item:GetSpecialValueFor("weapon_mult_stick")
		end
	end
	if equipKind == ITEM_KIND_MAGIC then
		if item:GetSpecialValueFor("weapon_mult_caster") ~= nil then
			itemMult = item:GetSpecialValueFor("weapon_mult_caster")
		end
	end
	if equipKind == ITEM_KIND_BOW then
		if item:GetSpecialValueFor("weapon_mult_bow") ~= nil then
			itemMult = item:GetSpecialValueFor("weapon_mult_bow")
		end
	end
	return itemMult
end